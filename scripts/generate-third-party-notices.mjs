import { readFile, writeFile } from "node:fs/promises";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const outputPath = join(root, "THIRD_PARTY_NOTICES.md");
const shippedNpmLicenses = new Set([
  "(Unlicense OR Apache-2.0)",
  "0BSD",
  "Apache-2.0",
  "ISC",
  "MIT",
]);

const goLicenses = new Map([
  ["github.com/dustin/go-humanize", "MIT"],
  ["github.com/google/uuid", "BSD-3-Clause"],
  ["github.com/mattn/go-isatty", "MIT"],
  ["github.com/ncruces/go-strftime", "MIT"],
  ["github.com/remyoudompheng/bigfft", "BSD-3-Clause"],
  ["golang.org/x/crypto", "BSD-3-Clause"],
  ["golang.org/x/sys", "BSD-3-Clause"],
  ["modernc.org/libc", "BSD-3-Clause"],
  ["modernc.org/mathutil", "BSD-3-Clause"],
  ["modernc.org/memory", "BSD-3-Clause"],
  ["modernc.org/sqlite", "BSD-3-Clause"],
]);

function goDependencies(source) {
  const dependencies = [];
  for (const match of source.matchAll(/^\s*([^\s]+)\s+(v[^\s]+)(?:\s+\/\/ indirect)?$/gm)) {
    const [, name, version] = match;
    const license = goLicenses.get(name);
    if (!license) throw new Error(`missing Go licence mapping for ${name}`);
    dependencies.push({ name, version, license });
  }
  return dependencies.sort((left, right) => left.name.localeCompare(right.name));
}

function npmPackageName(path) {
  return path.slice(path.lastIndexOf("node_modules/") + "node_modules/".length);
}

function npmDependencies(lock, enforceShippedCompatibility = false) {
  const dependencies = new Map();
  for (const [path, metadata] of Object.entries(lock.packages)) {
    if (!path.includes("node_modules/") || metadata.dev === true) continue;
    const name = npmPackageName(path);
    const license = metadata.license;
    if (typeof license !== "string" || license.length === 0) {
      throw new Error(`missing npm licence metadata for ${name}@${metadata.version}`);
    }
    if (enforceShippedCompatibility && !shippedNpmLicenses.has(license)) {
      throw new Error(`incompatible shipped npm licence ${license} for ${name}@${metadata.version}`);
    }
    dependencies.set(`${name}@${metadata.version}`, {
      name,
      version: metadata.version,
      license,
    });
  }
  return [...dependencies.values()].sort((left, right) =>
    `${left.name}@${left.version}`.localeCompare(`${right.name}@${right.version}`),
  );
}

function section(title, description, dependencies) {
  return [
    `## ${title}`,
    "",
    description,
    "",
    ...dependencies.map(
      ({ name, version, license }) => `- \`${name}@${version}\` — ${license}`,
    ),
    "",
  ].join("\n");
}

const [goMod, webLock, siteLock] = await Promise.all([
  readFile(join(root, "go.mod"), "utf8"),
  readFile(join(root, "web/package-lock.json"), "utf8").then(JSON.parse),
  readFile(join(root, "site/package-lock.json"), "utf8").then(JSON.parse),
]);

const output = [
  "# Third-party notices",
  "",
  "Local TOTP is licensed under Apache-2.0. The following direct and transitive packages are included in the compiled binary or embedded web application, or are used to generate the documentation site. Package licence texts remain available from their respective source distributions.",
  "",
  "This file is generated by `scripts/generate-third-party-notices.mjs`. Do not edit it manually.",
  "",
  section(
    "Go binary",
    "Modules linked into the Local TOTP binary:",
    goDependencies(goMod),
  ),
  section(
    "Embedded web application",
    "Production npm packages represented in `web/package-lock.json`:",
    npmDependencies(webLock, true),
  ),
  section(
    "Documentation site",
    "Packages required by the production site build in `site/package-lock.json`. Build tools in this section are not necessarily present in the generated static output:",
    npmDependencies(siteLock),
  ),
].join("\n");

if (process.argv.includes("--check")) {
  const current = await readFile(outputPath, "utf8");
  if (current !== output) {
    throw new Error("THIRD_PARTY_NOTICES.md is stale; regenerate it");
  }
} else {
  await writeFile(outputPath, output, "utf8");
}
